// ============================================================================
//! \page filePage File access documentation
//! \tableofcontents
//! The file access utilities provide a set of file management classes. \n
//!
//! \section secF1 File management utilities
//! The file management utilities implement basic file manipulation notions :
//! - already defined file access exceptions,
//! - basic file manipulation functions (ie file & path create/update/delete & access
//! functions) in the FileName class,
//! - temporary file classes (File, TempFileName classes),
//! - directory enumeration class.
//!
//! They also provide some specific features :
//! - a "Windows configuration" style file reader,
//! - a directory change watchdog.
//!
//! \section secF2 File access classes
//! Links to file access classes : \n
//!   - yat::FileName
//!   - yat::File
//!   - yat::TempFileName
//!   - yat::FileEnum
//!   - yat::CfgFile
//!   - yat::DirectoryWatcher
// ============================================================================

#endif // _FILE_H_
