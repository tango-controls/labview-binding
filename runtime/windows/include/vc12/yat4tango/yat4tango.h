#ifndef _YAT4TANGO_H_
#define _YAT4TANGO_H_

// ----------------------------------------------------------------------------
//! \mainpage YAT4Tango library
//! \tableofcontents
//!
//! yat4tango is Tango oriented specialization of the yat C++ framework.
//! Its provides the developper with powerful tools easing common implementation tasks.
//!
//! The topics dealt in YAT4Tango library are:
//!
//!   - Advanced Threading:
//!     - yat4tango::DeviceTask 
//!
//!   - Device Dynamic Interface: 
//!     - yat4tango::DynamicInterfaceManager (\subpage DynInterfacePage "see example")
//!     - yat4tango::DynamicCommand,
//!     - yat4tango::DynamicCommandManager
//!     - yat4tango::DynamicAttribute
//!     - yat4tango::DynamicAttributeManager
//!
//!   - Device and Attribute Monitoring:
//!     - yat4tango::MonitoredAttribute
//!     - yat4tango::MonitoredDevice
//!     - yat4tango::MonitoredDeviceTask
//!
//!   - Attribute Group:
//!     - yat4tango::AttributeGroup
//!     - yat4tango::AttributeGroupReply
//!
//!   - Logging: 
//!     - yat4tango::InnerAppender
//!     - yat4tango::TangoLogAdapter
//!     - yat4tango::TraceHelper
//!
//!   - Device Plugin (yat::Plugin extension/specialization):
//!     - yat4tango::DevicePlugIn
//!     - yat4tango::DevicePlugInHelper
//!
//!   - Properties Support:
//!     - yat4tango::PropertyHelper
//!
//!   - Exception Conversion Helpers:
//!     - yat4tango::YATDevFailed
//!     - yat4tango::TangoYATException
//!
//!   - The following classes are \b OBSOLETE: 
//!     - yat4tango::ThreadSafeDeviceProxy
//!     - yat4tango::ThreadSafeDeviceProxyHelper
//!
//! \remarks
//! The YAT library is a C++ general purpose library.
//!
// ----------------------------------------------------------------------------


#endif // _YAT4TANGO_H_
